var status = 0;
$(document).ready(function () {
	$(".version_upgrade_btn").addClass("xtxxclickstyle");
});
function updatakey(){
	if(status==0){
		$(".version_upgrade_btn_cartoon").css({
			"display":"block",
		});
		$(".version_upgrade_btn").removeClass("xtxxclickstyle");
		status=1;
	}else{
		$(".version_upgrade_btn_cartoon").css({
			"display":"none",
		});
		$(".version_upgrade_btn").addClass("xtxxclickstyle");
		status=0;
	}
	
}